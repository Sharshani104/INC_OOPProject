    private void btnSearchByIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchByIdActionPerformed
        if("".equals(txtpId.getText())){
            lblmessage.setText("Project Id cannot be null");
        }
        else{
             
            ProjectManager pm = new ProjectManager();
            Project p=pm.searchById(Integer.parseInt(txtpId.getText().trim()));
            
            txtpName.setText(p.getpname());
            txtpCost.setText(Double.toString(p.getpcost()));   
        }
    }//GEN-LAST:event_btnSearchByIdActionPerformed

