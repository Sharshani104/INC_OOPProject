    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        
        if("".equals(txtpId.getText())){
            
            lblmessage.setText("Project Id cannot be null");
            
        }
        else{
          Project project= new Project(Integer.parseInt(txtpId.getText().trim()),txtpName.getText().trim(),Float.parseFloat(txtpCost.getText().trim()));
          ProjectManager pm = new ProjectManager();
          boolean states = pm.insert(project);
          
          if(states == true){
             
              lblmessage.setText("Data inserted successfully");
          }
          else{
              lblmessage.setText("Data not inserted. Try Again!");
          }  
        }     
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
      if("".equals(txtpId.getText())){
           
          lblmessage.setText("Project Id cannot be null");
          
        }
      else{
        Project project = new Project(Integer.parseInt(txtpId.getText().trim()),txtpName.getText().trim(),Float.parseFloat(txtpCost.getText().trim()));
        ProjectManager pm = new ProjectManager();
        boolean states = pm.update(project); 
        
        if(states == true){
            
           lblmessage.setText("Data updated successfully"); 
        }
        else{
            
            lblmessage.setText("Data not updated. Try Again!");
            
        }
      }
      
      
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if("".equals(txtpId.getText())){
            
            lblmessage.setText("Project Id cannot be null");
            
        }
        else{
            ProjectManager pm = new ProjectManager();
            boolean states = pm.delete(Integer.parseInt(txtpId.getText())); 
            
            if(states == true){
                
                lblmessage.setText("Data deleted successfully");
                
            }
            else{
                
                lblmessage.setText("Data not deleted. Try Again!");
                
            }
        }
        
        
    }//GEN-LAST:event_btnDeleteActionPerformed

